function kele = EulerBeam(E,I,L,k)
ke = E*I/L^3*[12, 6*L, -12, 6*L;...
      6*L, 4*L^2, -6*L, 2*L^2;...
      -12, -6*L, 12, -6*L;...
      6*L, 2*L^2, -6*L, 4*L^2];

ks = k*[13/35*L, 11*L^2/210, 9/70*L, -13*L^2/420;...
      11*L^2/210, L^3/105, 13*L^2/420, -L^3/140;...
      9/70*L, 13*L^2/420, 13*L/35, -11*L^2/210;...
      -13*L^2/420, -L^3/140, -11*L^2/210, L^3/105];

kele = ke + ks;